num = int(input('Digite um número: '))
dobro = num*2
trilplo = num*3
raiz_quad = num**num

print('O número passado foi {}.\n'.format(num), 'O dobro de seu valor é: {},\n'.format(dobro),
      "O triplo de seu valor é: {},\n".format(trilplo), "E seu valor elevado ao quadrado é: {}".format(raiz_quad))